-- ============================================
-- ТИП: UnitStats (статические характеристики типа юнита)
-- ============================================
-- Описывает, какими свойствами обладает, например, "Скелет-воин" 
-- (в отличие от конкретного скелета, который сейчас стоит в слоте)
export type UnitStats = {
    -- Уникальное имя типа (например, "SkeletonWarrior", "Zombie", "IceGolem")
    Name: string,
    
    -- Базовое здоровье (при воскрешении)
    BaseHealth: number,
    
    -- Базовый урон (без баффов)
    BaseDamage: number,
    
    -- Дальность атаки: ближняя (8), средняя (20), дальняя (40)
    AttackRange: number,
    
    -- Тип атаки влияет на анимации и взаимодействие с броней врага
    AttackType: "Melee" | "Ranged" | "Magic",
    
    -- Стоимость в "душах" (чем сильнее юнит, тем дороже)
    SoulCost: number,
    
    -- Имя модели в Roblox (например, "http://www.roblox.com/asset/?id=123456789")
    ModelId: string,
}

-- ============================================
-- ТИП: UnitSlot (экземпляр юнита, стоящий в конкретном слоте)
-- ============================================
-- Это уже конкретный скелет, который принадлежит игроку,
-- имеет текущее здоровье (может быть раненым) и наложенные баффы
export type UnitSlot = {
    -- Уникальный ID этого юнита (генерируется при создании)
    UnitId: string,
    
    -- Ссылка на тип (отсылка к UnitStats)
    UnitType: string,
    
    -- ТЕКУЩЕЕ здоровье (может быть меньше BaseHealth, если юнит ранен)
    CurrentHealth: number,
    
    -- Список активных баффов (Ледяное прикосновение, Ядовитый покров и т.д.)
    -- Ключ = название баффа, значение = параметры (сила, длительность)
    Buffs: { [string]: any },
    
    -- Сколько "душ" потрачено на этого юнита (не превышает лимит слота)
    SoulPowerUsed: number,
}

-- ============================================
-- ТИП: PlayerData (все данные игрока, которые сохраняются между сессиями)
-- ============================================
export type PlayerData = {
    -- Уровень некроманта (влияет на количество слотов и лимит душ)
    Level: number,
    
    -- Сколько слотов доступно (на 1 уровне = 1, на 30 уровне = 5)
    MaxSlots: number,
    
    -- Максимум "душ" на один слот (на 1 уровне = 10, на 30 = 30)
    MaxSoulPerSlot: number,
    
    -- Текущая армия (ключ = номер слота 1..MaxSlots, значение = UnitSlot или nil)
    CurrentArmy: { [number]: UnitSlot? },
    
    -- Юниты в склепах (на хранении, не участвуют в бою)
    -- Ключ = уникальный ID, значение = UnitSlot
    StoredUnits: { [string]: UnitSlot },
    
    -- Уровень агрессии деревни (чем выше, тем сильнее атакуют твой погост)
    AggroLevel: number,
}

-- ============================================
-- ТИП: Buff (усиление, которое можно наложить на юнита)
-- ============================================
export type Buff = {
    -- Название (IceTouch, PoisonAura, Invisibility и т.д.)
    Name: string,
    
    -- Что делает бафф: урон, защита, скорость, невидимость, яд
    EffectType: "Damage" | "Defense" | "Speed" | "Invisibility" | "Poison",
    
    -- Сила эффекта (например, +10 к урону или -30% к скорости врага)
    Value: number,
    
    -- Длительность в секундах (-1 = бесконечно, до смерти юнита)
    Duration: number,
}